# Type Casting

# Integer
print("Integer Type Casting")
a = "12"
print(a, type(a))  # String
a = int(a)
print(a, type(a))  # Integer
a = 121.12
print(a, type(a))  # Float
a = int(a)
print(a, type(a))  # Integer
a = False
print(a, type(a))  # Boolean
a = int(a)
print(a, type(a))  # Integer


# Float
print("\n\nFloat Type Casting")
a = "1223.121"
print(a, type(a))
a = float(a)
print(a, type(a))
a = 11
print(a, type(a))
a = float(a)
print(a, type(a))


# String
print("\n\nString Type Casting")
a = 12223
print(a, type(a))
a = str(a)
print(a, type(a))
a = 122.23
print(a, type(a))
a = str(a)
print(a, type(a))
